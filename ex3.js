var dias = prompt("Digite um número de 1-7: ");

switch (dias){
case '1' :
alert ("O número 1 é domingo.")
break;
case '2' :
alert ("O número 2 é segunda.")
break;
case '3' :
alert ("O número 3 é terça.")
break;
case '4' :
alert ("O número 4 é quarta.")
break;
case '5' :
alert ("O número 5 é quinta.")
break;
case '6' :
alert ("O número 6 é sexta.")
break;
case '7' :
alert ("O número 7 é sábado.")
break;
}
